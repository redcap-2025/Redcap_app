// App Icon Generator Script
// This script helps generate app icons from the RedCap logo
// 
// Required icon sizes for Android:
// - mipmap-mdpi: 48x48
// - mipmap-hdpi: 72x72  
// - mipmap-xhdpi: 96x96
// - mipmap-xxhdpi: 144x144
// - mipmap-xxxhdpi: 192x192
//
// For iOS:
// - 20x20, 29x29, 40x40, 58x58, 60x60, 76x76, 80x80, 87x87, 120x120, 152x152, 167x167, 180x180
//
// Instructions:
// 1. Convert the SVG logo to PNG at 512x512 resolution
// 2. Use an online tool like https://appicon.co/ to generate all sizes
// 3. Place the generated icons in the appropriate folders:
//    - Android: android/app/src/main/res/mipmap-*/
//    - iOS: ios/Runner/Assets.xcassets/AppIcon.appiconset/
//
// The logo should be:
// - Square (1:1 aspect ratio)
// - High resolution (at least 512x512)
// - Transparent background
// - Clear and recognizable at small sizes
